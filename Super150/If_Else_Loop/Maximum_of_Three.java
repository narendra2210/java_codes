package Super150.If_Else_Loop;

public class Maximum_of_Three {
    public static void main(String[] args) {
	
		int a = 18;
		int b = 18;
		int c = 8;
		if (a >= b && a >= c) {
			System.out.println(a);
		}

		else if (b > c && b > a) {
			System.out.println(b);
		} else {
			System.out.println(c);
		}

	}
}
